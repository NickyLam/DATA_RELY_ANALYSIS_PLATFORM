  ,CERT_STATUS_CD
  ,MAIN_CERT_NO_FLG
  ,NETW_VRFCTION_FLG
  ,NETW_VRFCTION_REST_CD
  ,END_DT
  ,ID_MARK
  ,SRC_TABLE_NAME
  ,JOB_CD
FROM IML.PTY_PARTY_CERT_INFO_H_ECIF1_STATIC_DATA t1
where 1 = 1 
and not exists (select 1 from iml.pty_party_cert_info_h b 
                        where t1.party_id=b.party_id 
                          and t1.sorc_sys_cd=b.sorc_sys_cd 
                          and t1.cert_type_cd=b.cert_type_cd
                );
