  ,CERT_STATUS_CD
  ,MAIN_CERT_NO_FLG
  ,NETW_VRFCTION_FLG
  ,NETW_VRFCTION_REST_CD
  ,END_DT
  ,ID_MARK
  ,SRC_TABLE_NAME
  ,JOB_CD
FROM IML.PTY_PARTY_CERT_INFO_H
UNION ALL
SELECT PARTY_ID
  ,LP_ID
  ,SORC_SYS_CD
  ,CERT_TYPE_CD
  ,START_DT
  ,CERT_NUM
  ,CERT_ADDR
  ,ISSUE_CERT_ORG
  ,ISSUE_CERT_ORG_CTY_CD
  ,CERT_EFFECT_DT
  ,CERT_INVALID_DT
  ,LICEN_ISSUE_AUTHO_DIST_CD
